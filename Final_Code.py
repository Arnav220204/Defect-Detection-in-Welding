"""## Analysis 3 — Grad-CAM Heatmaps"""

def make_gradcam_heatmap(img_array, model, conv_layer_name, pred_index):
    grad_model = Model(
        inputs  = model.inputs,
        outputs = [model.get_layer(conv_layer_name).output, model.output]
    )
    with tf.GradientTape() as tape:
        conv_out, preds = grad_model(img_array)
        class_score     = preds[:, pred_index]
    grads        = tape.gradient(class_score, conv_out)
    pooled_grads = tf.reduce_mean(grads, axis=(0, 1, 2))
    heatmap      = conv_out[0] @ pooled_grads[..., tf.newaxis]
    heatmap      = tf.squeeze(heatmap)
    heatmap      = tf.maximum(heatmap, 0)
    max_val      = tf.math.reduce_max(heatmap)
    if max_val > 0:
        heatmap = heatmap / max_val
    return heatmap.numpy()

def overlay_gradcam(img_uint8, heatmap, alpha=0.4):
    h = cv2.resize(heatmap, (img_uint8.shape[1], img_uint8.shape[0]))
    h = np.uint8(255 * h)
    h = cv2.applyColorMap(h, cv2.COLORMAP_JET)
    h = cv2.cvtColor(h, cv2.COLOR_BGR2RGB)
    return cv2.addWeighted(img_uint8, 1 - alpha, h, alpha, 0)

