def preprocess_image(path):
    img = cv2.imread(path, cv2.IMREAD_GRAYSCALE)
    if img is None:
        return None
    img = cv2.resize(img, IMG_SIZE)
    img = np.stack((img,) * 3, axis=-1)   # grayscale -> 3-channel
    return img

def load_dataset(base_dir, json_file, samples_per_class=None, desc='Loading'):
    with open(json_file) as f:
        label_map = json.load(f)
    keys = list(label_map.keys())
    np.random.shuffle(keys)
    class_counts = [0] * NUM_CLASSES
    images, labels = [], []
    for key in tqdm(keys, desc=desc):
        label = label_map[key]
        if samples_per_class and class_counts[label] >= samples_per_class:
            continue
        path = os.path.join(base_dir, key)
        if not os.path.exists(path):
            continue
        img = preprocess_image(path)
        if img is None:
            continue
        images.append(img)
        labels.append(label)
        class_counts[label] += 1
    print(f'  {len(images)} images | counts: {class_counts}')
    return np.array(images, dtype=np.uint8), np.array(labels, dtype=np.int32)

